"""
Segment realization.
"""

import math

# ==================================================================================================


class Segment:
    """
    Segment realization.
    """

    # ----------------------------------------------------------------------------------------------

    def __init__(self, a, b):
        """
        Constructor.

        :param a: First point.
        :param b: Second point.
        """

        self.Points = [a, b]

    # ----------------------------------------------------------------------------------------------

    def __str__(self):
        """
        String representation.

        :return: String.
        """

        return 'Segment [{0} - {1}]'.format(self.Points[0], self.Points[1])

    # ----------------------------------------------------------------------------------------------

    def a(self):
        """
        A point.

        :return: A point.
        """

        return self.Points[0]

    # ----------------------------------------------------------------------------------------------

    def b(self):
        """
        B point.

        :return: B point.
        """

        return self.Points[1]

# ==================================================================================================
