
class Vect:
    """
    Vector of three coordinates.
    """

    # ----------------------------------------------------------------------------------------------

    def __init__(self, x=0.0, y=0.0, z=0.0):
        """
        Constructor.
        :param x: coord X
        :param y: coord Y
        :param z: coord Z
        """

        self.X = x
        self.Y = y
        self.Z = z

    # ----------------------------------------------------------------------------------------------
